package PrimitiveDataTypes;

public class PrimitiveDTypes {
	static int salary = 200000;
	static short temperature = -1000;
	static long distance = 345;
	static byte age = 45;
	static float pi = 4.13f;
	static double D = 5.98765432;
	static char grade = 'A';
	static boolean B = true;
	static String name = "vishal";
	
	
	public static void main(String[] args) {
		int salary = 200000;
		short temperature = -1000;
		long distance = 345;
		byte age = 45;
		float pi = 4.13f;
		double D = 5.98765432;
		char grade = 'A';
		boolean B = true;
		String name = "vishal";
		
		
		System.out.println("Salary is : " +salary);
		System.out.println("Temperature is : " +temperature);
		System.out.println("Distance is : " +distance);
		System.out.println("Age is : " +age);
		System.out.println("Value of pi is : " +pi);
		System.out.println("Value of d is : " +D);
		System.out.println("name is : " +grade);
		System.out.println("Boolean is : " +B);
		System.out.println("String name is : " +name);
		
		
		
	}
	}


